#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	cmp;

	cmp = 0;
	while (cmp == 0 && (*s1 != '\0' || *s2 != '\0'))
	{
		cmp = *s1 - *s2;
		s1++;
		s2++;
	}
	return (cmp);
}

