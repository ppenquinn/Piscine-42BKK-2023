int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	cmp;

	cmp = 0;
	while (cmp == 0 && (*s1 != '\0' || *s2 != '\0') && n > 0)
	{
		cmp = *s1 - *s2;
		s1++;
		s2++;
		n--;
	}
	return (cmp);
}

