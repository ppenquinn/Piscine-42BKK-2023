#include <stdio.h>
#include <unistd.h>

void	ft_print_combn(int num)
{
	char	ans[num];
	int 	i;

	i = 0;
	while (ans[i])
		ans[i] = '0';
}

int	main(void)
{
	ft_print_combn();
}
