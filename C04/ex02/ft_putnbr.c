#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	ans;

	if (nb < 0 && nb > -10)
		write(1, "-", 1);
	if (nb >= 10 || nb <= -10)
	{
		ft_putnbr(nb / 10);
		nb %= 10;
	}
	if (nb >= 0)
		ans = '0' + nb;
	else
		ans = '0' - nb;
	write(1, &ans, 1);
}

